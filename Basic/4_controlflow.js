// switch statement
//control flow

const month = "jan" ;

switch (month) {
    case "jan":
    console.log("january");
    break;
    case 2:
    console.log("February");
        break;
    case 3:
    console.log("March");
        break;
    case 4:
    console.log("April");
        break;

    default:
        console.log("default case match");
        break;
}